#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*b2;

	i = -1;
	b2 = b;
	while (++i < len)
	{
		b2[i] = c;
	}
	return (b);
}
