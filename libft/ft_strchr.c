#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	c2;

	c2 = (char) c;
	while (*s)
	{
		if (*s == c2)
			return ((char *) s);
		++s;
	}
	if (c2 == 0)
		return ((char *) s);
	return (0);
}
